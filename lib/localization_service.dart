  // Método para obtener mensaje de categoría traducido
  String getCategoryMessage(String categoryId) {
    final translationKey = _categoryToTranslationKey[categoryId] ?? 'msgOther';
    return t(translationKey);
  }

  // Método para obtener nombre de categoría traducido
  String getCategoryName(String categoryId) {
    final translationKey = _categoryIdToTranslationKey[categoryId] ?? 'categoryOther';
    return t(translationKey);
  }

  // Obtener lista de categorías con sus IDs
  List<MapEntry<String, String>> getCategories() {
    return _categoryIdToTranslationKey.entries
        .map((entry) => MapEntry(entry.key, getCategoryName(entry.key)))
        .toList();
  }

  // Traducción con parámetros
  String tr(String key, {Map<String, dynamic>? args}) {
    String translation = t(key);
    if (args != null) {
      args.forEach((key, value) {
        translation = translation.replaceAll('{$key}', value.toString());
      });
    }
    return translation;
  }

